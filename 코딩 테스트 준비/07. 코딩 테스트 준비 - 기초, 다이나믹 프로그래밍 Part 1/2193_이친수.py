# 문제 번호: 2193
# 문제 제목: 이친수
# 문제 설명:
#   - 이친수는 0으로 시작하지 않으며 1이 두 번 연속으로 나타나지 않는 이진수를 말합니다.
#   - 주어진 N에 대해 N자리 이친수의 개수를 구하는 문제입니다.

# 입력:
#   - 첫째 줄에 N이 주어집니다. (1 ≤ N ≤ 90)

# 출력:
#   - 길이가 N인 이친수의 개수를 출력합니다.

# N은 이친수의 자릿수 (1 <= N <= 90)
N = int(input())

# dp 테이블
# dp[i]는 길이가 i인 이친수의 개수를 나타냅니다.
dp = [0] * (N + 1)

# 초기 상태 설정
dp[1] = 1 # 길이가 1인 이친수는 1개, 즉 "1"만 가능

if N > 1:
    dp[2] = 1 # 길이가 2인 이친수는 "10"만 가능

# dp 계산
for i in range(3, N + 1):
    dp[i] = dp[i - 1] + dp[i - 2]  # 길이가 i인 이친수의 개수는 dp[i-1] + dp[i-2]

# 결과 출력 
print(dp[N])